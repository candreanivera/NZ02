// Data types - Primitive
// Number - No necesita quotation marks
let age = 21;
// Imprime el tipo de dato de la variable
console.log(typeof age);

// String - Para palabras
// Utiliza quotation marks, puede ser " o '
let name = "Cristina";
console.log(name);

// boolean
let isStudent = false;
console.log(typeof isStudent);

// undefined - uninitialized or mossing value (Aun no sé que variable será)
let city;
console.log(typeof city);

// null - intentional absence or any object
let car = null;
console.log(typeof car);
